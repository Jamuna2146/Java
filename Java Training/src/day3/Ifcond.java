package day3;

public class Ifcond {

	public static void main(String[] args) {
		
		int num = 20;
		
		if(num==20) {
			System.out.println("num is equal to 20");
		};
		if(num<30) {
			System.out.println("num is less than 30");
		};
		if(num>5) {
			System.out.println("num is greater than 5");
		}
		else{
			System.out.println("Nothing");
		};
	}

}
