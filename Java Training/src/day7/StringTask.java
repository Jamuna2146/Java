package day7;

import java.lang.String;;

public class StringTask {

	public static void main(String[] args) {
		
		StringBuffer sb = new StringBuffer("Hello World");
		System.out.println(sb);
		
		sb.reverse();
		System.out.println(sb);
		
		String str = sb.toString();
		str.split(" ");
		System.out.println(str);
		
		
		
		
		
	}

}
